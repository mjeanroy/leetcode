const isValidSudoku = require('./index');

describe('isValidSudoku', () => {
  it('should not be valid with a non valid row', () => {
    const board = [
      ["5","3",".",".","7",".",".",".","."],
      ["6",".",".","1","9","6",".",".","."],
      [".","9","8",".",".",".",".","6","."],
      ["8",".",".",".","6",".",".",".","3"],
      ["4",".",".","8",".","3",".",".","1"],
      ["7",".",".",".","2",".",".",".","6"],
      [".","6",".",".",".",".","2","8","."],
      [".",".",".","4","1","9",".",".","5"],
      [".",".",".",".","8",".",".","7","9"],
    ];

    expect(isValidSudoku(board)).toBe(false);
  });

  it('should not be valid with a non valid column', () => {
    const board = [
      ["5","3",".",".","7",".",".",".","."],
      ["6",".",".","1","9","5",".",".","."],
      [".","9","8",".",".",".",".","6","."],
      ["8",".",".",".","6",".",".",".","3"],
      ["4",".",".","8",".","3",".",".","1"],
      ["7",".",".",".","2",".",".",".","6"],
      [".","6",".",".",".",".","2","8","."],
      [".",".",".","8","1","9",".",".","5"],
      [".",".",".",".","8",".",".","7","9"],
    ];

    expect(isValidSudoku(board)).toBe(false);
  });

  it('should not be valid with a non valid square', () => {
    const board = [
      ["5","3",".",".","7",".",".",".","."],
      ["6",".",".","1","9","7",".",".","."],
      [".","9","8",".",".",".",".","6","."],
      ["8",".",".",".","6",".",".",".","3"],
      ["4",".",".","8",".","3",".",".","1"],
      ["7",".",".",".","2",".",".",".","6"],
      [".","6",".",".",".",".","2","8","."],
      [".",".",".","8","1","9",".",".","5"],
      [".",".",".",".","8",".",".","7","9"],
    ];

    expect(isValidSudoku(board)).toBe(false);
  });

  it('should not be valid with a non valid square', () => {
    const board = [
      ["5","3",".",".","7",".",".",".","."],
      ["6",".",".","1","9","5",".",".","."],
      [".","9","8",".",".",".",".","6","."],
      ["8",".",".",".","6",".",".",".","3"],
      ["4",".",".","8",".","3",".",".","1"],
      ["7",".",".",".","2",".",".",".","6"],
      [".","6",".",".",".",".","2","8","."],
      [".",".",".","4","1","9",".",".","5"],
      [".",".",".",".","8",".",".","7","9"],
    ];

    expect(isValidSudoku(board)).toBe(true);
  });
});
